using System;

namespace Assignment
{
    class Program
    {
        static void Main(string[] args)
        {
            // C# Assignment Operators
            int a;
            a = 10;
            Console.WriteLine($"a=10: {a}");
            a += 5;
            Console.WriteLine($"a+=5: {a}");
            a -= 5;
            Console.WriteLine($"a-=5: {a}");
            a *= 5;
            Console.WriteLine($"a*=5: {a}");
            a /= 5;
            Console.WriteLine($"a/=5: {a}");
        }
    }
}
